package com.pwc.springboot.model;

public class BagicPojo {

	String vechileTyp;
	String proposalType;
	String existingPolicyNo;
	String firstName;
	String middleName;
	String surName;
	String phone;

	public String getVechileTyp() {
		return vechileTyp;
	}

	public void setVechileTyp(String vechileTyp) {
		this.vechileTyp = vechileTyp;
	}

	public String getProposalType() {
		return proposalType;
	}

	public void setProposalType(String proposalType) {
		this.proposalType = proposalType;
	}

	public String getExistingPolicyNo() {
		return existingPolicyNo;
	}

	public void setExistingPolicyNo(String existingPolicyNo) {
		this.existingPolicyNo = existingPolicyNo;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getMiddleName() {
		return middleName;
	}

	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}

	public String getSurName() {
		return surName;
	}

	public void setSurName(String surName) {
		this.surName = surName;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

}
